// returns the number of elements in an array

let firstArray = []
console.log(firstArray.length) // returns 0

let secondArray = [0, 1, 2, 3, 4]
console.log(secondArray.length) // returns 5

let thirdArray = [1, 2, 3, 4, 5]
console.log(thirdArray.length) // returns 5

let fourthArray = ['a', 'b', 'c']
console.log(fourthArray.length) // returns 3

let fifthArray = ['a', 3, {name: "apple", fruit: true, vegetable: false}]
console.log(fifthArray.length) // returns 3

let sixthArray = [[2, 4, 6]]
console.log(sixthArray.length) // returns 1

let seventhArray = ['dog', 'pig', [2, 4, 6]]
console.log(seventhArray.length) // returns 3